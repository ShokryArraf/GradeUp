2. הנח חבילות קרח להפחתת נפיחות (הנח בד בין הקרח לעור).
3. הרם את האזור הפגוע (אם אפשרי) כדי להפחית נפיחות.
4. עצור דימום (אם יש):
   - לחץ בעדינות עם בד נקי.
