class EmergencyInstructions {
  // CPR Instructions
  static const String cprEnglish = '''
1. Check the scene for safety and confirm unresponsiveness.
2. Call emergency services (e.g., 911 or equivalent) or ask someone else to call.
3. Place the person on their back on a firm surface.
4. Start chest compressions:
   - Place the heel of one hand on the center of the chest.
   - Place the other hand on top and interlock your fingers.
   - Push hard and fast at a rate of 100–120 compressions per minute.
5. Rescue breaths (if trained):
   - Tilt the head back and lift the chin.
   - Pinch the nose and give 2 breaths, each lasting 1 second.
6. Continue cycles of 30 compressions and 2 breaths until help arrives or the person recovers.
  ''';

  static const String cprHebrew = '''
1. וודא שהמקום בטוח ושהאדם אינו מגיב.
2. התקשר לשירותי חירום (101 במד"א) או בקש ממישהו אחר להתקשר.
3. השכב את האדם על הגב על משטח יציב.
4. התחל עיסוי חזה:
   - הנח את בסיס כף היד במרכז החזה.
   - הנח את כף היד השנייה מעל ושזור את האצבעות.
   - לחץ חזק ומהר בקצב של 100–120 לחיצות לדקה.
5. נשימות הצלה (אם מאומן):
   - הרם את הראש לאחור והגב את הסנטר.
   - צבט את האף ותן 2 נשימות, כל נשימה למשך שנייה אחת.
6. המשך במחזורים של 30 לחיצות ו-2 נשימות עד שהעזרה מגיעה או עד שהאדם מתאושש.
  ''';

  // Burn Instructions
  static const String burnsEnglish = '''
1. Stop the burning process:
   - Remove the person from the source of the burn.
   - Extinguish flames or cool chemical burns with water.
2. Cool the burn:
   - Rinse with cool (not cold) water for 10-20 minutes.
   - Do not apply ice or very cold water.
3. Protect the burn:
   - Cover with a sterile, non-stick bandage or clean cloth.
   - Avoid breaking blisters.
4. Seek medical help for severe burns, burns on sensitive areas (face, hands, genitals), or if the burn is larger than the palm.
  ''';

  static const String burnsHebrew = '''
1. עצור את תהליך הכווייה:
   - הרחק את האדם ממקור החום.
   - כבה להבות או שטוף כוויות כימיות במים.
2. קרר את הכווייה:
   - שטוף במים קרירים (לא קרים מאוד) למשך 10–20 דקות.
   - אל תשתמש בקרח או במים קפואים.
3. הגן על הכווייה:
   - כסה עם תחבושת סטרילית שאינה נדבקת או בד נקי.
   - הימנע מניקוב שלפוחיות.
4. פנה לעזרה רפואית במקרה של כוויות חמורות, כוויות באזורים רגישים (פנים, ידיים, איברי מין) או אם הכווייה גדולה מכף היד.
  ''';

  // Fracture Instructions
  static const String fracturesEnglish = '''
1. Immobilize the injured area:
   - Do not move the person unless necessary.
   - Use a splint or sling to immobilize the limb.
2. Apply ice packs to reduce swelling (place a cloth between the ice and skin).
3. Elevate the injured area (if possible) to reduce swelling.
4. Control bleeding (if any):
   - Apply gentle pressure with a clean cloth.
5. Seek immediate medical attention for open fractures, deformities, or severe pain.
  ''';

  static const String fracturesHebrew = '''
1. קבע את האזור הפגוע:
   - אל תזיז את האדם אלא אם כן זה הכרחי.
